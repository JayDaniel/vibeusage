#!/usr/bin/env node
"use strict";

const fs = require("node:fs/promises");
const path = require("node:path");

async function main() {
  const args = new Set(process.argv.slice(2));
  const check = args.has("--check");
  if (args.has("--help") || args.has("-h")) {
    process.stdout.write(
      [
        "Usage:",
        "  node scripts/build-supabase-functions.cjs [--check]",
        "",
        "Outputs:",
        "  supabase-functions/*.js (single-file deploy artifacts)",
        "",
        "Notes:",
        "  - Sources live in supabase-src/functions/*.js",
        "  - Artifacts are generated and should not be edited by hand.",
        "",
      ].join("\n"),
    );
    return;
  }

  const rootDir = path.resolve(__dirname, "..");
  const srcDir = path.join(rootDir, "supabase-src", "functions");
  const outDir = path.join(rootDir, "supabase-functions");

  const entryPoints = await listEntryPoints(srcDir);
  if (entryPoints.length === 0) {
    throw new Error(`No entry points found under ${path.relative(rootDir, srcDir)}/`);
  }

  if (!check) {
    await removeStaleOutputs({ outDir, entryPoints });
  }

  const esbuild = require("esbuild");

  // Plugin: 将 @supabase/supabase-js 重定向到 esm.sh CDN（Deno 兼容）
  const supabaseExternalPlugin = {
    name: "supabase-external",
    setup(build) {
      build.onResolve({ filter: /^@supabase\/supabase-js$/ }, () => ({
        path: "https://esm.sh/@supabase/supabase-js@2",
        external: true,
      }));
    },
  };

  const result = await esbuild.build({
    entryPoints,
    outdir: outDir,
    entryNames: "[name]",
    bundle: true,
    format: "esm",
    platform: "browser",
    target: ["es2020"],
    sourcemap: false,
    legalComments: "none",
    plugins: [supabaseExternalPlugin],
    banner: {
      js: "// Generated by scripts/build-supabase-functions.cjs. DO NOT EDIT.\n",
    },
    logLevel: "silent",
    write: !check,
  });

  if (!check) {
    process.stdout.write(
      `Built ${entryPoints.length} Supabase edge functions into ${path.relative(rootDir, outDir)}/\n`,
    );
    return;
  }

  const problems = await diffOutputs({ rootDir, outDir, outputFiles: result.outputFiles || [] });
  if (problems.length > 0) {
    process.stderr.write("supabase-functions is out of date:\n");
    for (const p of problems) process.stderr.write(`- ${p}\n`);
    process.exitCode = 1;
    return;
  }

  process.stdout.write("supabase-functions is up to date.\n");
}

async function listEntryPoints(srcDir) {
  const entries = await fs.readdir(srcDir, { withFileTypes: true }).catch(() => []);
  return entries
    .filter((e) => e.isFile() && e.name.endsWith(".js"))
    .map((e) => path.join(srcDir, e.name))
    .sort();
}

async function diffOutputs({ rootDir, outDir, outputFiles }) {
  const problems = [];
  for (const out of outputFiles) {
    const rel = path.relative(rootDir, out.path);
    if (!rel.startsWith(`supabase-functions${path.sep}`)) continue;

    const disk = await fs.readFile(out.path).catch(() => null);
    if (!disk) {
      problems.push(`${rel} (missing)`);
      continue;
    }
    if (!buffersEqual(disk, out.contents)) {
      problems.push(`${rel} (differs)`);
    }
  }

  const expected = new Set(
    outputFiles
      .map((o) => path.relative(outDir, o.path))
      .filter((p) => p && !p.includes("..") && p.endsWith(".js")),
  );
  const existing = await fs.readdir(outDir, { withFileTypes: true }).catch(() => []);
  for (const e of existing) {
    if (!e.isFile() || !e.name.endsWith(".js")) continue;
    if (!expected.has(e.name)) {
      problems.push(`${path.join("supabase-functions", e.name)} (unexpected file)`);
    }
  }

  return problems;
}

async function removeStaleOutputs({ outDir, entryPoints }) {
  const expected = new Set(entryPoints.map((entry) => `${path.parse(entry).name}.js`));
  const existing = await fs.readdir(outDir, { withFileTypes: true }).catch(() => []);
  for (const entry of existing) {
    if (!entry.isFile() || !entry.name.endsWith(".js")) continue;
    if (expected.has(entry.name)) continue;
    await fs.rm(path.join(outDir, entry.name));
  }
}

function buffersEqual(a, b) {
  if (!a || !b) return false;
  if (a.length !== b.length) return false;
  for (let i = 0; i < a.length; i++) {
    if (a[i] !== b[i]) return false;
  }
  return true;
}

main().catch((err) => {
  process.stderr.write(`${err && err.stack ? err.stack : String(err)}\n`);
  process.exitCode = 1;
});
